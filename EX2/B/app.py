RCON = [0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36]

def sub_word(word):
    return (S_BOX[(word >> 24) & 0xFF] << 24) | (S_BOX[(word >> 16) & 0xFF] << 16) | \
           (S_BOX[(word >> 8) & 0xFF] << 8) | S_BOX[word & 0xFF]

def rot_word(word):
    return ((word << 8) & 0xFFFFFFFF) | (word >> 24)

def key_expansion(key_hex):
    words = []
    for i in range(4):
        words.append(int(key_hex[i*8:(i+1)*8], 16))
    for i in range(4, 44):
        temp = words[i-1]
        if i % 4 == 0:
            temp = sub_word(rot_word(temp)) ^ (RCON[i//4] << 24)
        words.append(words[i-4] ^ temp)
    round_keys = []
    for i in range(0, 44, 4):
        rk = []
        for j in range(4): rk.extend(words[i+j].to_bytes(4, 'big'))
        round_keys.append(rk)
    return round_keys

def sub_bytes(state):
    for i in range(16): state[i] = S_BOX[state[i]]

def shift_rows(s):
    res = [0]*16
    res[0], res[4], res[8], res[12] = s[0], s[4], s[8], s[12]
    res[1], res[5], res[9], res[13] = s[5], s[9], s[13], s[1]
    res[2], res[6], res[10], res[14] = s[10], s[14], s[2], s[6]
    res[3], res[7], res[11], res[15] = s[15], s[3], s[7], s[11]
    for i in range(16): s[i] = res[i]

def xtime(a):
    return ((a << 1) ^ 0x1B) & 0xFF if a & 0x80 else (a << 1) & 0xFF

def mix_columns(s):
    for i in range(0, 16, 4):
        s0, s1, s2, s3 = s[i], s[i+1], s[i+2], s[i+3]
        s[i] = xtime(s0 ^ s1) ^ s1 ^ s2 ^ s3
        s[i+1] = xtime(s1 ^ s2) ^ s2 ^ s3 ^ s0
        s[i+2] = xtime(s2 ^ s3) ^ s3 ^ s0 ^ s1
        s[i+3] = xtime(s3 ^ s0) ^ s0 ^ s1 ^ s2

def add_round_key(state, key):
    for i in range(16): state[i] ^= key[i]

def format_state(state):
    return bytes(state).hex().upper()

def encrypt(plaintext_hex, key_hex):
    state = list(bytes.fromhex(plaintext_hex))
    round_keys = key_expansion(key_hex)
    log = []

    log.append(f"--- START ENCRYPTION ---")
    log.append(f"Input Plaintext: {plaintext_hex.upper()}")
    
    # Round 0
    add_round_key(state, round_keys[0])
    log.append(f"Round 0 (Initial AddRoundKey): {format_state(state)}")

    # Rounds 1-9
    for i in range(1, 10):
        log.append(f"\n--- ROUND {i} ---")
        sub_bytes(state)
        log.append(f"After SubBytes:   {format_state(state)}")
        shift_rows(state)
        log.append(f"After ShiftRows:  {format_state(state)}")
        mix_columns(state)
        log.append(f"After MixColumns: {format_state(state)}")
        add_round_key(state, round_keys[i])
        log.append(f"After Round Key:  {format_state(state)}")

    # Final Round (Round 10)
    log.append(f"\n--- FINAL ROUND (10) ---")
    sub_bytes(state)
    log.append(f"After SubBytes:   {format_state(state)}")
    shift_rows(state)
    log.append(f"After ShiftRows:  {format_state(state)}")
    add_round_key(state, round_keys[10])
    log.append(f"After Round Key:  {format_state(state)}")
    
    log.append(f"\nFINAL CIPHERTEXT: {format_state(state)}")
    return log

